# Q1] Program to display my details

#Taking information from the user

#Input for int And float

n1 = int(input("Enter a num n1 :"))
n2 = int(input("Enter a num n2 :"))
f1 = float(input("Enter a num f1 :"))
f2 = float(input("Enter a num f2 :"))

#Displaying the output

print("sum of int",n1+n2)
print("sub of int",n1-n2)
print("mul of int",n1*n2)
print("div of int",n1/n2)
print("mod of int",n1%n2)
print("exp of int",n1**n2)

print("sum of float",f1+f2)
print("sub of float",f1-f2)
print("mul of float",f1*f2)
print("div of float",f1/f2)
print("mod of float",f1%f2)
print("exp of float",f1**f2)

